import sys
import random
import ast
import json
import pandas as pd
from pathlib import Path

LOCAL_CSV = Path(__file__).parent / "tmdb_5000_movies.csv"
OUT_PATH = Path(__file__).parent.parent / "catalog.py"


def fetch_csv():
    if not LOCAL_CSV.exists():
        print("ERROR: tmdb_5000_movies.csv not found.")
        print(f"Expected at: {LOCAL_CSV}")
        print()
        print("Download it from Kaggle:")
        print("  https://www.kaggle.com/datasets/tmdb/tmdb-movie-metadata")
        print("Then place tmdb_5000_movies.csv in the data/ folder and re-run.")
        sys.exit(1)
    print(f"Reading local CSV: {LOCAL_CSV}")
    return LOCAL_CSV.read_text(encoding="utf-8")


def parse_genres(raw):
    try:
        return [g["name"] for g in ast.literal_eval(raw)]
    except Exception:
        return []


def sample_catalog(df):
    df = df.dropna(subset=["title", "popularity", "vote_count", "overview", "release_date"])
    df = df.copy()
    df["popularity"] = pd.to_numeric(df["popularity"], errors="coerce")
    df["vote_count"] = pd.to_numeric(df["vote_count"], errors="coerce")
    df = df.dropna(subset=["popularity", "vote_count"])
    df = df.drop_duplicates(subset=["id"])

    top500 = df.nlargest(500, "popularity")
    remainder = df.drop(top500.index)
    median_pop = df["popularity"].median()
    long_tail = remainder[remainder["popularity"] < median_pop]
    non_tail = remainder[remainder["popularity"] >= median_pop]

    random.seed(42)
    n_random = min(200, len(non_tail))
    random_200 = non_tail.sample(n=n_random, random_state=42)

    random.seed(123)
    n_tail = min(300, len(long_tail))
    tail_300 = long_tail.sample(n=n_tail, random_state=123)

    combined = pd.concat([top500, random_200, tail_300]).drop_duplicates(subset=["id"])
    return combined


def derive_signals(df):
    min_pop = df["popularity"].min()
    max_pop = df["popularity"].max()
    min_votes = df["vote_count"].min()
    max_votes = df["vote_count"].max()

    catalog = []
    for _, row in df.iterrows():
        pop_norm = (row["popularity"] - min_pop) / (max_pop - min_pop) if max_pop != min_pop else 0.0
        vote_norm = (row["vote_count"] - min_votes) / (max_votes - min_votes) if max_votes != min_votes else 0.0

        try:
            release_year = int(str(row["release_date"])[:4])
        except Exception:
            release_year = 0

        genres = parse_genres(row.get("genres", "[]"))
        overview = str(row.get("overview", ""))[:300]

        catalog.append({
            "id": int(row["id"]),
            "title": str(row["title"]),
            "genres": genres,
            "overview": overview,
            "release_year": release_year,
            "mock_popularity": round(vote_norm, 6),
            "mock_ctr": round(0.05 + (pop_norm * 0.45), 6),
            "mock_search_freq": round(pop_norm, 6),
            "mock_abandonment": round(0.60 - (pop_norm * 0.50), 6),
        })
    return catalog


def write_catalog(catalog):
    lines = ["# Auto-generated by data/fetch_catalog.py — do not edit manually\n"]
    lines.append(f"CATALOG = {json.dumps(catalog, indent=2, ensure_ascii=False)}\n")
    OUT_PATH.write_text("".join(lines), encoding="utf-8")
    print(f"catalog.py written: {len(catalog)} titles → {OUT_PATH}")


if __name__ == "__main__":
    csv_text = fetch_csv()
    from io import StringIO
    df = pd.read_csv(StringIO(csv_text))
    print(f"Raw dataset: {len(df)} rows")
    sampled = sample_catalog(df)
    print(f"Sampled: {len(sampled)} titles")
    catalog = derive_signals(sampled)
    write_catalog(catalog)
